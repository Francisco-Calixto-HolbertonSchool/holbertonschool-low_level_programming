#include "calc.h"
#include <stdio.h>

/**
 * op_add - Short description
 * op_sub - jjj
 * op_mul - kkk
 * op_div - jjjjd
 * op_mod - jjaja
 * @a: First member
 * @b: Second member
 * Return: iwf
 * jfjfjf
 * Description: Longer description
 */

int op_add(int a, int b)
{
	if (a && b)
		return (a + b);
}
int op_sub(int a, int b)
{
        if (a && b)
		return (a - b);
}
int op_mul(int a, int b)
{
        if (a && b)
		return (a * b);
}
int op_div(int a, int b)
{
        if (a && b)
		return (a / b);
}
int op_mod(int a, int b)
{
        if (a && b)
		return (a % b);
}
